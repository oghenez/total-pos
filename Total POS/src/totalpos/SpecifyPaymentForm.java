    private void tableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableKeyPressed
        if ( evt.getKeyCode() == KeyEvent.VK_A || evt.getKeyCode() == KeyEvent.VK_ESCAPE){
            this.dispose();
        }else if ( evt.getKeyCode() == KeyEvent.VK_F12 ){
            payForms.remove(table.getSelectedRow());
            updateAll();
        }else if ( evt.getKeyCode() == KeyEvent.VK_N ){
            AddMoney2Pay am2p = new AddMoney2Pay(this, true, "Nota de Credito");
            Shared.centerFrame(am2p);
            am2p.setVisible(true);
        } else if ( evt.getKeyCode() == KeyEvent.VK_E ){
            AddMoney2Pay am2p = new AddMoney2Pay(this, true, "Efectivo");
            Shared.centerFrame(am2p);
            am2p.setVisible(true);
        } else if ( evt.getKeyCode() == KeyEvent.VK_C ){
            AddCard2Pay ac2p = new AddCard2Pay(this, true, "Credito");
            if ( ac2p.isOk ){
                Shared.centerFrame(ac2p);
                ac2p.setVisible(true);
            }
        } else if ( evt.getKeyCode() == KeyEvent.VK_D ){
            AddCard2Pay ac2p = new AddCard2Pay(this, true, "Debito");
            if ( ac2p.isOk ){
                Shared.centerFrame(ac2p);
                ac2p.setVisible(true);
            }
        } else if (  evt.getKeyCode() == KeyEvent.VK_O ){
            try {
                if ( change < 0 ){
                    MessageBox msb = new MessageBox(MessageBox.SGN_CAUTION, "Monto insuficiente.");
                    msb.show(null);
                }else{
                    myParent.printer.printerSerial = null;
                    if (!myParent.printer.checkPrinter()) {
                        MessageBox msb = new MessageBox(MessageBox.SGN_DANGER, "La impresora no coincide con la registrada en el sistema. No se puede continuar");
                        msb.show(null);
                        return;
                    }
                    
                    myParent.print(payForms);
                    add("Cambio",change);
                    ConnectionDrivers.savePayForm(payForms);
                    this.dispose();
                }
            } catch (SQLException ex) {
                MessageBox msb = new MessageBox(MessageBox.SGN_DANGER, "Error en la base de datos!",ex);
                msb.show(null);
                this.dispose();
                Shared.reload();
            } catch (FileNotFoundException ex) {
                what2DoWithReceipt();
                this.dispose();
                Shared.reload();
            } catch (Exception ex) {
                what2DoWithReceipt();
                this.dispose();
                Shared.reload();
            }
        }
    }//GEN-LAST:event_tableKeyPressed

    private void what2DoWithReceipt(){
        Object[] options = {"Cancelarla","Poner en espera"};
        int n = JOptionPane.showOptionDialog(this,"Error al imprimir, por favor, indique que hacer con la factura.",
                Constants.appName,
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
        if ( n == 0 ){
            myParent.deleteCurrent();
        }else if ( n == 1 ){
            try {
                myParent.toWait();
                myParent.updateAll();
            } catch (SQLException ex1) {
                MessageBox msb = new MessageBox(MessageBox.SGN_DANGER, "Problemas con la base de datos.",ex1);
                msb.show(null);
            }
        }
    }

